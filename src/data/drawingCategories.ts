export const getRandomDrawingWord = (categoryId: string): { word: string; category: DrawingCategory | null } => {
  const category = drawingCategories.find(c => c.id === categoryId);
  if (!category || category.words.length === 0) {
    return { word: "", category: null };
  }
  const word = category.words[Math.floor(Math.random() * category.words.length)];
  return { word, category };
};

