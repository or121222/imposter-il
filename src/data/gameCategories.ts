export const getRandomWordPair = (categoryId: string, customCategories: Category[] = []): { wordA: string; wordB: string } => {
  const allCategories = [...gameCategories, ...customCategories];
  const category = allCategories.find(c => c.id === categoryId);
  if (!category || category.wordPairs.length === 0) {
    return { wordA: "", wordB: "" };
  }
  const pair = category.wordPairs[Math.floor(Math.random() * category.wordPairs.length)];
  return pair;
};

// Legacy function for backward compatibility
export const getRandomWord = (categoryId: string): string => {
  const { wordA } = getRandomWordPair(categoryId);
  return wordA;
};

